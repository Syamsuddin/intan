<li>Pengumpulan bahan penyusunan pedoman dan petunjuk teknis keprotokolan dan perjalanan dinas;</li>
<li>Inventarisasi dan dokumentasi serta distribusi bahan-bahan penerbitan.</li></p>
</ol>
<p>Tugas-tugas tersebut mempunyai rincian sebagai berikut :<br/>
<ol>
<li>Menyusun program, mengatur dan mengawasi penyiapan dan pengolahan data/bahan dan petunjuk teknis pengumpulan dan penyaringan informasi;</li>
<li>Menyusun program, mengatur dan mengawasi penyiapan dan pengolahan data/bahan dan petunjuk teknis serta pelaksanaan penyajian dan pemberitaan ke media cetak maupun elektronik;</li>
<li>Menyusun program, mengatur dan mengawasi pelaksanaan kegiatan keprotokolan;</li>
<li>Menyusun program, mengatur dan mengawasi penyiapan dan pengolahan data/bahan serta pelaksanaan publikasi kebijakan pemerintah daerah dan dokumentasi kegiatan pemerintah, pembangunan dan kemasyarakatan;</li>
<li>Melaksanakan tugas lain yang diberikan oleh Administrasi Pembangunan dan Kemasyarakatan sesuai dengan bidang tugas.</li>
</ol>
