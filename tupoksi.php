<h3>Tupoksi Organisasi</h3>
<p>Berdasarkan Peraturan Bupati Hulu Sungai Selatan Nomor 018 Tahun 2009 tentang Tugas Pokok, Fungsi, Rincian Tugas dan Tata Kerja Unsur-Unsur Organisasi Sekretariat Daerah Kabupaten Hulu Sungai Selatan, Bagian Hubungan Masyarakat dan Protokol mempunyai tugas melaksanakan pembinaan hubungan kemasyarakatan guna memperjelas kebijakan Pemerintah Daerah dan melaksanakan kegiatan keprotokolan.</p>
<p>Untuk melaksanakan tugas sebagaimana dimaksud di atas, Bagian Hubungan Masyarakat dan Protokol mempunyai fungsi sebagai berikut: <br/>
<ol>
